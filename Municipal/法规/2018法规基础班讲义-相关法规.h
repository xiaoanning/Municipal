#pragma mark 评标
/*
 评标
 
 1.评标委员会
     评标由招标人依法组建的评标委员会负责。
     依法必须进行招标的项目，其评标委员会由招标人的代表和有关技术、 经济等方面的专家组成，成员人数为5人以上单数(含5人)，其中经济、技术专家不少于成员总数的2/3。
     评标委员会成员的名单在中标结果确定前应当保密。
 （>=5人单数 + >2/3 ）
 
 2.评标
     评标委员会经评审，认为所有投标都不符合招标文件要求的，可以否决所有投标。
     依法必须进行招标的项目的所有投标被否决的，招标人应当依法重新招标。
 
     评标委员会成员应当依照招标投标法和本条例的规定，按照招标文件规定的评标标准和方法，客观、公正地对投标文件提出评审意见。
     招标文件没有规定的评标标准和方法不得作为评标的依据。
 
     评标委员会成员不得私下接触投标人，不得收受投标人给予的财物或者其他好处，不得向招标人征询确定中标人的意向，不得接受任何单位或者个人明示或者暗示提出的倾向或者排斥特定投标人的要求，不得有其他不客观、不公正履行职务的行为。
 
 3.标底
     招标项目设有标底的，招标人应当在开标时公布。
     标底只能作为评标的参考，不得以投标报价是否接近标底作为中标条件，也不得以投标报价超过标底上下浮动范围作为否决投标的条件。
 
 4.否决投标（必考）
     1投标文件未经投标单位盖章和单位负责人签字;（都没有的否决，有一个就不能否决）
     2投标联合体没有提交共同投标协议;
     3投标人不符合国家或者招标文件规定的资格条件;
     4同一投标人提交两个以上不同的投标文件或者投标报价，但招标文件要求提交备选投标的除外;
     5投标报价低于成本或者高于招标文件设定的最高投标限价;
     6投标文件没有对招标文件的实质性要求和条件作出响应;
     7投标人有串通投标、弄虚作假、行贿等违法行为。
 
 5.投标人对投标文件的澄清和说明 （必考）
     评标委员会可以要求投标人对投标文件中含义不明确的内容作必要澄清或说明，但是澄清或者说明不得超出投标文件的范围或改变投标文件的实质性内容。
     投标文件中有含义不明确的内容、明显文字或者计算错误，评标委员会认为需要投标人作出必要澄清 、说明的 ，应当书面通知该投标人 。
 
     投标人的澄 清、说明应当采用书面形式，并不得超出投标文件的范围或者改变投标文件的实质性内容。
     评标委员会不得暗示或者诱导投标人作出澄清、说明，不得接受投标人主动提出的澄清、说明。
 
 6.评标后工作
     评标完成后， 评标委员会应当向招标人提交书面评标报告和中标候选人名 单。中标候选人应当不超过3个，并标明排序。（3个以下的候选人）
     评标报告应当由评标委员会全体成员签字。对评标结果有不同意见的评标委员会成员应当以书面形式说明其不同意见和理由，评标报告应当注明该不同意见。
     评标委员会成员拒绝在评标报告上签字又不书面说明其不同意见和理由的，视为同意评标结果。
 
 
 */

