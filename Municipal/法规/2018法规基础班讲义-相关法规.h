#pragma mark 工伤保险(强制)和意外伤害保险(鼓励)的规定
/*
 
 一、工伤保险的规定
     《工伤保险条例》规定，中国境内的企业、事业单位、社会团体、民办非企业单位、基金会、律师事务所、会计师事务所等组织和有雇工的个体工商户应当参加工伤保险。
 (一)工伤保险基金
     工伤保险基金由【用人单位缴纳的工伤保险费】、【工伤保险基金的利息】和依法纳入工伤保险基金的【其他资金】构成。
     用人单位应当按时缴纳工伤保险费。职工个人不缴纳工伤保险费。
     工伤保险基金存入社会保障基金财政专户，用于工伤保险待遇，劳动能力鉴定，工伤预防的宣传、培训等费用，以及法律、法规规定的用于工伤保险的其他费用的支付。
     任何单位或者个人不得将工伤保险基金用于投资运营、兴建或者改建办公场所、发放奖金，或者挪作其他用途。
 (二)工伤认定
     职工有下列情形之一的，应当认定为工伤:
         (1)在工作时间和工作场所内，因工作原因受到事故伤害的;
         (2)工作时间前后在工作场所内，从事与工作有关的预备性或者收尾性工作受到事故伤害的;
         (3)在工作时间和工作场所内，因履行工作职责受到暴力等意外伤害的;
         (4)患职业病的;
         (5)因工外出期间，由于工作原因受到伤害或者发生事故下落不明的;
         (6) 在上下班途中，受到非本人主要责任的交通事故或者城市轨道交通、客运 轮渡、火车事故伤害的;
         (7)法律、行政法规规定应当认定为工伤的其他情形。
     职工有下列情形之一的，视同工伤:
         (1)在工作时间和工作岗位，突发疾病死亡或者在48小时之内经抢救无效死亡的;
         (2)在抢险救灾等维护国家利益、公共利益活动中受到伤害的;
         (3)职工原在军队服役，因战、因公负伤致残，已取得革命伤残军人证，到用人单位后旧伤复发的。
     职工符合以上的规定，但是有下列情形之一的，不得认定为工伤或者视同工伤:
         (1)故意犯罪的;
         (2)醉酒或者吸毒的;
         (3)自残或者自杀的。
     (在施工现场斗殴受伤;施工期间醉酒坠落致残;在办公场所内因劳资纠纷自杀;因失恋，上班时间爬到公司楼顶跳楼自杀;精神抑郁，酗酒过度需要进行治疗;在工作场所与上司产生摩擦，拿剪刀将自己的胸脯刺伤。——均不是工伤。)
 
     职工发生事故伤害或者职业病，所在单位应当自事故伤害发生之日或者鉴定为职业病之日起30日内，提出工伤认定申请。用人单位未按以上规定提出工伤认定申请的，工伤职工或者其近亲属、工会组织在事故伤害发生之日或者鉴定为职业病之日起1年内，可以直接提出工伤认定申请 (——单位30日，个人1年)。
     用人单位未在以上规定的时限内提交工伤认定申请，在此期间发生的工伤待遇等有关费用由该用人单位负担。
 
     职工或者其近亲属认为是工伤，用人单位不认为是工伤的，由用人单位承担举证责任。
 
     社会保险行政部门应当自受理工伤认定申请之日起60日内作出工伤认定的决定。
     社会保险行政部门对受理的事实清楚、权利义务明确的工伤认定申请，应当在15日内作出工伤认定的决定
 
 二、建筑意外伤害保险的规定
     《建筑法》规定，鼓励企业为从事危险作业的职工办理意外伤害保险，支付保险费。（被保险人为从事危险作业人员）
     施工单位应当为施工现场从事危险作业的人员( 高处、深基坑、爆破作业)办理意外伤害保险。
     意外伤害保险费由施工单位支付。
     实行施工总承包的，由总承包单位支付意外伤害保险费。
 
     意外伤害保险期限自建设工程开工之日起至竣工验收合格止。
     保险费应当列入建筑安装工程费用。
     施工企业应在工程项目开工前，办理完投保手续。
 
 */
