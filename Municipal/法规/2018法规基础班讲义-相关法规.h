#pragma mark - ************** 建设工程代理制度

#pragma mark 三方当事人，两个法律关系
/*
    建设工程代理制度
 (三方当事人，两个法律关系)
 被代理人委托代理人在代理权限内，以被代理人的名义与第三人实施具有法律意义的行为，被代理人对代理行为承担民事责任
 
 特征:
     1. 代理权限范围;         代理人实施代理行为时有独立进行意思表示的权利，否则，只能视为传达意思表示的使者
     2. 以被代理人名义;
     3. 法律意义;
     4. 后果归属于被代理人。
 */

#pragma mark 代理的种类
/*
 代理的种类：
     委托代理
         按照被代理人的委托行使代理
         招标代理、采购代理、诉讼代理
     法定代理
         根据法律的规定行使代理权
         只针对无民事行为能力人和限制民事行为能力人  监护人是其法定代理人
         监护人行使监护代理权
     指定代理
         有关单位或法院指定
         对无民事或限制民事行为能力的人指定代理
 
 
 
 建设工程的承包活动不得委托代理。
         招标活动、诉讼活动、采购活动可委托代理
         禁止转包、主体结构禁止分包
 委托代理，可以用书面形式，也可以用口头形式，但法律规定用书面的，应该用书面。
 授权委托书应载明:
     代理人的姓名或名称、
     代理事项、权限和期间，
     并由委托人签名或盖章。
 委托书授权不明的，被代理人应当向第三人承担民事责任，代理人负连带责任
 
 
 有些委托代理，代理人必须具备相应的资格才能代理
         比如招标代理
 */

#pragma mark 代理关系的终止
/*
    委托代理终止的情形和法定代理终止的情形易混（代理人或被代理人死亡导致的代理终止情形）

    委托代理终止 ：
        代理期限届满或代理事务完成
        被代理人取消委托或代理人辞去委托
        代理人死亡或丧失民事行为能力
        作为被代理人或者代理人的法人终止
 
    法定/指定代理终止：
        被代理人取得或恢复民事行为能力
        指定代理的人民法院或指定单位取消指定
        被代理人或代理人死亡
        代理人丧失民事行为能力
        由于其他原因引起的被代理人和代理人之间的监护关系消灭

注意：被代理人死亡，委托代理关系不一定终止
 */

#pragma mark 无权代理和表见代理 转代理 复代理
/*
 
 注意各自的法律效果
 
 无权代理与表见代理(区别:1、无权代理:无权，待定; 2、表见代理:无权，有效)
 表见代理是无权代理的一种特殊情况
 
 无权代理三种情形:
    1自始未经授权;
    2超越代理权;
    3代理权已终止。
 无权代理只有经过被代理人的追认，被代理人才承担民事责任。
 未经追认的行为，由行为人承担民事责任
 【无权代理的后果是效力待定】
 
 表见代理四个条件:
    1无权代理;
    2善意相对人有理由相信行为人有代理权的，
        情形包括行为人持有本人发出的委任状、已加盖公章的空白合同书或者有显示本人向向行为人授予代理权的通知函告等证明文件;
    3 本人有过失;
    4相对人为善意
 
 法律效果:无权代理追认有效
 表见代理该行为有效(被代理人无拒绝权，可追偿)
 
 【表见代理是一种特殊的无权代理】
 
 
 转代理:事先需同意，事后追认，紧急情况，为保护被代理人利益除外。（必须紧急且保护利益）
         事先取得同意的，有效代理
         事先未取得同意的，效力待定（紧急情况除外）
 复代理:代理人为处理代理事物，为被代理人选任其他人进行代理被称为复代理。
 
 知道他人以本人名义实施不做否认表示的视为同意
 */

#pragma mark 代理的责任承担（ 代理中的四个连带责任）
/*
 谁有错谁担责 都有错承担连带责任
 
 代理中的四个连带责任
 1. 委托书授权不明，被代理人应当向第三人承担民事责任，代理人负连带责任;
 2. 代理人和第三人串通，损害被代理人利益的，代理人和第三人负连带责任;
 3. 第三人知道行为人无权代理还与之实施民事行为，第三人和行为人连带责任;
 4. 违法代理行为，被代理人和代理人连带。
 
 1，4代理内部出问题; 2，3代理外部出问题。
 */



