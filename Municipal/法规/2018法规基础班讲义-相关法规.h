#pragma mark 行政诉讼制度
/*
 
 第一审程序
     管辖:最初做出地法院，复议改变两被告，人身自由原被告，最先立案不动产
     起诉:具体行政行为之日6个月或复议决定15日
     审理:不适用调解，行政赔偿、补偿、自由裁量权例外
 
 判决
     驳回原告诉讼请求
     撤销判决，并可判决重新作出行政行为
     履行判决:不作为行为
     变更判决:行政处罚显失公平或及对款额认定有错误
 
 第二审程序
     上诉期限:判决15日或裁定10日
     上诉判决:维持原判;依法改判;发回重审或改判
 
 再审程序:向上一级人民法院
 执 行:公民、法人拒绝履行判决、裁定的，行政机关可以向 第一审人民法院申请强制执行，或者依法强制执行。
 国家赔偿:行使职权，人身权和财产权;个人行为和自己行为国家不赔
 
 
 对于不接收起诉状、接收起诉状后不出具书面凭证，以及不一次性告知 当事人需要补正的起诉状内容的，当事人可以向上级人民法院投诉
 人民法院既不立案，又不作出不予立案裁定的，当事人可以向上一级人民法院起诉
 行政行为所依据的国务院部门和地方人民政府及其部门制定的规范性文 件(不含规章)不合法，提起诉讼时，可以一并请求对该规范性文件进行审查
 
 行政案件的审理程序
         (1)行政争议未经行政复议的，应当自知道作出具体行政行为之日起6个月内起诉
         (2)经过行政复议但对行政复议决定不服而依法提起行政诉讼的， 应当在收到行政复议决定书之日起15日内起诉
         (3)法院审理行政案件不适用调解，但行政赔偿诉讼可以调解
         (4)行政诉讼期间，除该法规定的情形外，不停止具体行政行为的 执行。
             除涉及国家秘密、个人隐私和法律另有规定的外，人民法院应当公开审理行政案件。
             涉及商业秘密的案件，当事人申请不公开审理的， 可以不公开审理。
             人民法院审理行政案件，不适用调解。但是，行政赔偿、补偿以及行政机关行使法律、法规规定的自由裁量权的案件可以调 解。
         (5)人民法院审理行政案件，以【法律和行政法规、地方性法规】为依据。
             审理民族自治地方的行政案件，并以该民族自治地方的自治条例和 单行条例为依据。人民法院审理行政案件，参照规章。
 
 
 行政诉讼与民事诉讼比较
         (1)在行政诉讼中，被告对其作出的具体行政行为负有举证责任(民事诉讼"谁主张，谁举证”)
         (2)在行政诉讼过程中，被告不得自行向原告和证人收集证据
         (3)行政诉讼上诉审不受上诉范围限制，应进行全面审查(民事诉讼只审查上诉请求的范围)
         (4)必须组成合议庭(民事诉讼简易程序独任审理)
         (5)不能调解(行政赔偿诉讼可以调解，民事诉讼都可以调解)
 
     侵权的赔偿责任
     行使行政职权时侵权的赔偿责任
     赔偿诉讼可以适用调解。
     行政机关及其工作人员在行使行政职权时侵犯人身权和财产权的行为，受害人有取得赔偿的权利。
 属于下列情形之一的，国家不承担赔偿责任:
         1行政机关工作人员与行使职权无关的个人行为;
         2因公民、法人和其他组织自己的行为致使损害发生的;
         3法律规定的其他情形。
 */














