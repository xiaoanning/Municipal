#pragma mark 承揽合同
/*
 承揽合同的法律规定
 
 承揽合同是承揽人按照定定作人的要求完成工作，交付工作成果，定作人给付报酬的合同。
 承揽包括加工、订作、修理、复制、测试、检验等工作。
 
 4S店修车、加工零件均为承揽合同。车主——定作人，4S店——承揽人。
 
 一、承揽合同的特征
     (一)承揽合同以完成一定的工作并交付工作成果为标的
     (二)承揽人须以自己的设备、技术和劳力完成所承揽的工作
     (三)承揽人工作具有独立性
 承揽人在完成工作过程中，不受定作人的指挥管理，独立承担完成合同约定的质量、数量、期限等责任。
 承揽人在工作期间，应当接受定作人必要的监督检查。
 
 二、承揽合同当事人的权利义务:
     承揽合同是双务合同。
 
 (一)承揽人的义务
     1.按照合同约定完成承揽工作的义务
     2.材料检验的义务
         承揽人提供材料的，承揽人应当按照约定选用材料，并接受定作人检验。
         定作人提供材料的，承揽人应当对定作人提供的材料及时检验，发现不符合约定时，应当及时通知定作人更换、补齐或者采取其他补救措施。
     3.通知和保密的义务
     4.接受监督检查和妥善保管工作成果等的义务
         承揽人应当妥善保管定作人提供的材料以及完成的工作成果，因保管不善造成毁损、灭失的，应当承担损害赔偿责任。
     5.交付符合质量要求工作成果的义务
         承揽人交付的工作成果不符合质量要求的，定作人可以要求承揽人承担修理、重作、减少报酬、赔偿损失等违约责任。
     共同承揽人对定作人承担连带责任。
 (二)定作人的义务
     1.按照约定提供材料和协助承揽人完成工作的义务
     2.支付报酬的义务
         定作人应当按照约定的期限支付报酬。期限没有约定→协议补充→交易习惯
         定作人应当在承揽人交付工作成果时支付;
         工作成果部分交付的，定作人应当相应支付。除当事人另有约定的外，定作人未向承揽人支付报酬或者材料费等价款的，承揽人对完成的工作成果享有留置权。
     3.依法赔偿损失的义务
         定作人中途变更承揽工作的要求，造成承揽人损失的，应当赔偿损失。
         承揽人发现定作人提供的图纸或者技术要求不合理的，在通知定作人后，因定作人怠于答复等原因造成承揽人损失的，定作人应当赔偿损失。
     4.验收工作成果的义务
 三、承揽合同的解除
 (一)承揽人的法定解除权
     定作人不履行协助义务致使承揽工作不能完成的，承揽人可以催告定作人在合理期限内履行义务，并可以顺延履行期限;定作人逾期不履行的，承揽人可以依法解除合同。
 (二)定作人的法定解除权
     (经定作人同意)承揽人将其承揽的主要工作交由第三人完成的，应当就该第三人完成的工作成果向定作人负责;未经定作人同意的，定作人可以解除合同。
     承揽人将承揽的辅助工作交由第三人完成的，应当就第三人完成的工作成果向定作人负责。
 (三)定作人的法定任意解除权
     ——与施工合同不同 定作人可以随时解除承揽合同，造成承揽人损失的，应当赔偿损失。
 
 
 */

#pragma mark 买卖合同的法律规定

/*
 19min
 
  一、买卖合同的法律特征
     (一)买卖合同是一种转移财产所有权(——占有、使用、收益、处分)的合同
     (二)买卖合同是有偿合同
     (三)买卖合同是双务合同
     (四)买卖合同是诺成合同
     买卖合同可以是书面的，也可以是口头的。
     房屋买卖等标的额较大的财产买卖，应当签订书面合同。
 二、买卖合同当事人的权利义务
 (一)出卖人的主要义务
 1. 按照合同约定交付标的物的义务;
     (1)现实交付; 一手交货一手交钱
     (2)简易交付; 买方提前占有交易物
     (3)占有改定; 出卖后又租回
     (4)指示交付; 物品被第三人占有 指示第三人交付
     (5)拟制交付。交易票据类
 2. 转移标的物的所有权的义务;
     除法律另有约定或当事人另有约定以外，标的物的所有权自标的物交付时起转移。
     但是出卖具有知识产权的计算机软件等标的物的，除法律另有约定或当事人另有约定以外，该标的物的知识产权不属于买受人。
 3.瑕疵担保义务
     出卖人的瑕疵担保义务，可分为权利瑕疵担保义务和物的瑕疵担保义务。
     (1)权利瑕疵担保义务
         如果出卖人对于出卖的标的物没有所有权或处分权，或者没有完全的所有权或处分权，或者其处分涉及第三人的物权、知识产权等权益，则称其标的物存在权利瑕疵，出卖人因此应当承担权利瑕疵担保责任。
         买受人有确切证据证明第三人可能就标的物主张权利的，可以中止支付相应的价款，但出卖人提供适当担保的除外。
     (2)物的瑕疵担保义务
         出卖人就其所交付的标的物具有约定或法定品质所负有的担保义务。
 
 三、标的物毁损灭失风险的承担
 
     标的物毁损、灭失的风险，在标的物交付之前由出卖人承担，交付之后由买受人承担。
 
     因买受人的原因致使标的物不能按照约定的期限交付的，买受人应当自违反约定之日起承担标的物毁损、灭失的风险。
 
     出卖人出卖交由承运人运输的在途标的物，除当事人另有约定的以外，毁损、灭失的风险自合同成立时起由买受人承担。
     但在合同成立时出卖人知道或者应当知道标的物已经毁损、灭失却未告知买受人的，出卖人应当负担标的物毁损、灭失的风险。
 
     对于需要运输的标的物，当事人没有约定交付地点或约定不明确，出卖人将标的物交付给第一承运人后，标的物毁损、灭失的风险由买受人承担。
     出卖人依约将标的物置于交付地点，买受人违反约定没有收取的，标的物毁损、灭失的风险自违反约定之日起由买受人承担。
 
 四、特殊买卖合同的规定
     凭样品买卖:封存样品。
     试用买卖:适用买卖期间可以购买标的物，也可以拒绝购买。届满，买受人对是否购买标的物未作表示的，视为购买。
 五、孳息的归属和买卖合同的解除
     1、归属:交付。（交付时发生转移）
         法定孳息:租金、利息。
     2、解除:主物解除及于从物，从物不及于主物
         数物中，一物解除，其他物受损，数物也可解除
         多批物品中，一批解除，视关联程度解除其他批次
         分期付款，未支付价款达到五分之一，对方可要求支付全部价款或者解除合同，同时要求支付该物品使用费。
 
 
 */

#pragma mark 借款合同的法律规定

