#pragma mark ***** 建设工程竣工验收制度
/*
 竣工验收主体
     建设单位收到建设工程竣工报告后，应组织设计、施工、监理等单位进行竣工验收
 
 建设工程竣工验收应具备条件 （不包括竣工相关的文件）
        完成建设工程设计和合同约定的各项内容
        有完整的技术档案和施工管理资料
        有工程使用的主要建筑材料、建筑构配件和设备的进场试验报告【不包括建筑装饰辅助材料】
        有勘察、设计、施工、工程监理等单位分别签署的质量合格文件
        有施工单位签署的工程保修书
 
 
 二、竣工工程质量争议的处理
         《质量条例》规定，施工单位对施工中出现质量问题的建设工程或者竣工验收不合格的建设工程，应当负责返修。
         据此，建设工程竣工时发现的质量问题或者质量缺陷，无论是建设单位的责任还是施工单位的责任，施工单位都有义务进行修复或返修。
         但是，对于非施工单位原因出现的质量问题或质量缺陷，其返修的费用和造成的损失是应由责任方承担的。
 (1)承包方责任的处理
         《解释》规定，因承包人的过错造成建设工程质量不符合约定，承包人拒绝修理、返工或者改建，发包人请求减少支付工程价款的，应予支持。
 (2)发包方责任的处理
         《解释》规定，发包人具有下列情形之一，造成建设工程质量缺陷，应当承担过错责任:
         1)提供的设计有缺陷;
         2)提供或者指定购买的建筑材料、建筑构配件、设备不符合强制性标准;
         3)直接指定分包人分包专业工程。
 (3)未经竣工验收擅自使用的处理
        《建筑法》规定，建设工程竣工经验收合格后，方可交付使用。
        《解释》规定，建设工程未经竣工验收，发包人擅自使用后，又以使用部分质量不符合约定为由主张权利的，不予支持;
        但是承包人应当在建设工程的合理使用寿命内对地基基础工程和主体结构质量承担民事责任
 
 */
#pragma mark ***** 建设工程质量保修制度
/*
     建设工程质量保修制度，是指建设工程竣工经验收后，在规定的保修期限内，因勘察、设计、施工、材料等原因造成的质量缺陷，应当由【施工承包单位】负责维修、返工或更换，由【责任单位】负责赔偿损失的法律制度。
     建设工程承包单位在向建设单位提交工程竣工验收报告时，应当向建设单位出具质量保修书。
     质量保修书中应明确保修范围、保修期限、保修责任等。
     因建设单位或者用户使用不当或擅自改动结构、设备位置以及不当装修等造成质量问题的，施工单位不承担保修责任;由此而造成的质量受损或者其他用户损失，应当由责任人承担相应的责任。
 
 【在规定的保修期限内，因勘察、设计、施工、材料等原因造成的质量缺陷】
 【使用不当或擅自改动结构、设备位置以及不当装修等造成质量问题的】
 
 
 最低保修期限：（竣工验收合格之日起）（这里是最低保修期  大于这个年限都是符合要求的）
         基础设施、房屋建筑地基基础和主体结构工程   —— 合理使用年限
         屋面防水、有防水要求的卫生间、房间和外墙面的防渗漏  ——  5年
         供热和供冷系统    ——  2个采暖期、供冷期
         电气管线、给排水管道、设备安装和装修 ——  2年
 
 因建设单位或用户使用不当或擅自改动结构、设备位置、第三方、不可抗力等，施工单位不保修
 超出合理使用年限后如需继续使用，产权所有人应委托具有资质的勘察、设计单位鉴定，并根据鉴定结果加固、维修等，重新界定使用期。违法使用，后果产权人自负
 
 
 《质量条例》规定，建设工程在保修范围和保修期限内发生质量问题的，施工单位应当履行保修义务，并对造成的损失承担赔偿责任。
 因保修人未及时履行保修义务，导致建筑物损毁或者造成人身、财产损害的，保修人应当承担赔偿责任。
 保修人与建筑物所有人或者发包人对建筑物毁损均有过错的，各自承担相应的责任。
 
 质量损失赔偿责任的承担
         施工单位过错，施工单位返修并赔偿
         设计问题，先由施工单位维修，再通过建设单位向设计单位追偿
         材料问题，先由施工单位维修，谁采购谁承担责任
         建设单位问题，先由施工单位维修，责任建设单位承担;监理单位问题，建设单位向监理单位索赔
         使用不当问题，先由施工单位维修，使用单位负责
         自然灾害损坏，先由施工单位维修，各方根据国家政策分担
 
总结:谁过错谁承担，先由施工单位维修为原则
 */
