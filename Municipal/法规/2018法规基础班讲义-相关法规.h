#pragma mark 法律体系框架 （建筑法属于行政法）
/*
 【重点】
 某法律部门包括哪些法律
 某部法律属于哪个法律部门

 
 部门法 根据一定标准、原则所制定的同类法律规范的总称
 
 一、宪法及宪法相关法
     宪法相关法
         是指《中国人民代表大会组织法》、《地方各级人民代表大会和地方各级人民政府组织法》、《中华人民共和国国籍法》、《中华人民共和国国务院组织法》、《中华人民共和国民族区域自治法》等法律
 
 二、民法商法
     民法是规定并调整平等主体的公民间、法人间及公民与法人间的财产关系和人身关系的法律规范的总称。
     商法是调整市场经济关系中商人及其商事活动的法律规范的总称。
     我国采用的是民商合一的立法模式。商法被认为是民法的特别法和组成部分。
     《民法通则》、《合同法》、《物权法》、《侵权责任法》、《公司法》、《招标投标法》等属于民法商法
 
 三、行政法
     作为行政法调整对象的行政关系，主要包括行政管理关系、行政法制监督关系、行政救济关系、内部行政关系 。
     《行政处罚法》、《行政复议法》、《行政许可法》、《环境影响评价法》、《城市房地产管理法》、《城乡规划》、《建筑法》等属于行政法。
 
 四、经济法
     经济法是调整在国家协调、干预经济运行的过程中发生的经济关系的法律规范的总称。
     《统计法》、《土地管理法》、《标准化法》、《税收征收管理法》、《预算法》、《审计法》、【《节约能源法》】、《政府采购法》、《反垄断法》等属于经济法。
 五、社会法
     社会法是调整劳动关系、社会保障和社会福利关系的法律规范的总称。
     《残疾人保障法》、《矿山安全法》、《劳动法》、《职业病防治法》、《安全生产法》、《劳动合同法》等属于社会法。
     《劳动合同法》≠《合同法》 ；《合同法》属于民法商法
 
 六、刑法
     《刑法》是这一法律部门的主要内容。
 
 七、诉讼与非诉讼程序法
 
 我国三大诉讼法 《民事诉讼法》、《行政诉讼法》、《刑事诉讼法》
 非诉讼程序法主要是《仲裁法》
 
 
 【经济法和民商法是同一等级】
 宪法及相关法             宪法、组织法、选举法、民族区域自治法、国籍法
 民商法 (【平等】)           民法通则、物权法、合同法、侵权责任法、公司法、招投标法
 行政法 (【政府】)           行政处罚法、行政复议法、行政许可法、环境影响评价法、城市房地产管理法、城乡规划法、建筑法
 经济法 (【调控】)           统计法、土地管理法、标准化法、税收征收法、预算法、审计法、节约能源法、政府采购法、反垄断法
 社会法 (【弱者】)           残疾人保障法、矿山安全法、劳动法、职业病防治法、安全生产法、劳动合同法
 刑法                   刑法(犯罪、刑罚)
 诉讼与非诉讼程序法       民事诉讼法、行政诉讼法、刑事诉讼法、仲裁法
 */

