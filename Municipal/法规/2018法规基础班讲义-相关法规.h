#pragma mark 租赁合同的法律规定

/*
  一、租赁合同的法律特征
     租赁合同是转移租赁物使用收益权(——占有、使用、收益，无处分)的合同，也是诺成合同和双务、有偿合同。
 二、租赁合同的内容和类型
     (二)租赁合同的类型
         租赁合同根据租赁标的物不同，可分为动产租赁和不动产租赁。
         此外，根据是否约定租赁期限，还可分为定期租赁和不定期租赁。
 
         租赁合同可以约定租赁期限，但租赁期限不得超过20年。超过20年的， 超过部分无效。
         租赁期限6个月以上的，应当采用书面形式。当事人未采用书面形式的，视为不定期租赁。不定期租赁，当事人可以随时解除合同。
 三、租赁合同当事人的权利义务
     (一)出租人的义务
         出租人的主要义务是交付出租物、维修租赁物、权利瑕疵担保、物的瑕疵担保、保证承租人优先购买权和保证共同居住人继续承租。
         除当事人另有约定的外，出租人应当履行租赁物的维修义务。
         出租人出卖租赁房屋的，应当在出卖之前的合理期限内通知承租人，承租人享有以同等条件优先购买的权利。
         租赁物在租赁期间发生所有权变动的，不影响租赁合同的效力。——买卖不破租赁
     (二)承租人的义务
         承租人的主要义务是支付租金、按照约定使用租赁物、妥善保管赁物、有关事项通知、返还租赁物和损失赔偿。
         承租人应当按照约定的期限支付租金。
 
         承租人经出租人同意，可以将租赁物转租给第三人。
         承租人转租的，承租人与出租人之间的租赁合同继续有效，第三人对租赁物造成损失的，承租人应当赔偿损失。
         承租人未经出租人同意转租的，出租人可以解除合同。
 
 */

#pragma mark 融资租赁合同的法律规定

/*
 融资租赁合同的法律规定
 《合同法》规定，融资租赁合同是出租人根据承租人对出卖人、租赁物的选择，向出卖人购买租赁物，提供给承租人使用，承租人支付租金的合同。
 
 
 一、融资租赁合同的法律特征
     (一)出租人身份的二重性
         出租人是租赁行为的出租方，又是买卖关系的买受人。
         承租人将其自有物出卖给出租人，再通过融资租赁合同将租赁物从出租人处租回的，人民法院不应仅以承租人和出卖人系同一人为由认定不构成融资租赁法律关系。
     (二)出卖人权利与义务相对人的差异性
         承租人享有买受人的权利但不承担买受人的义务。
         出租人不负担租赁物的维修与瑕疵担保义务，但承租人须向出租人履行交付租金义务。
     (三)融资租赁合同是要式合同
         《合同法》规定，融资租赁合同应当采用书面形式。
 二、融资租赁合同当事人的权利义务
     (一)出租人的义务
         出租人应当根据承租人对出卖人、租赁物的选择订立的买卖合同，向出卖人支付标的物的价金。
         出租人应当保证承租人对租赁物的占有和使用。
 
         出租人、出卖人、承租人可以约定，出卖人不履行买卖合同义务的，由承租人行使索赔的权利。
         承租人行使索赔权利的，出租人应当协助。
 
         出租人根据承租人对出卖人、租赁物的选择订立的买卖合同，未经承租人同意，出租人不得变更与承租人有关的合同内容。
         租赁物不符合约定或者不符合使用目的的，出租人不承担责任，但承租人依赖出租人的技能确定租赁物或者出租人干预选择租赁物的除外。
 
     (二)出卖人的义务
         出卖人的主要义务是向承租人交付标的物、标的物的瑕疵担保。
         出卖人违反合同约定的向承租人交付标的物的义务，承租人因下列情形之一拒绝受领租赁物的，人民法院应予支持:
         (1)租赁物严重不符合约定的;
         (2)出卖人未在约定的交付期间或者合理期间内交付租赁物，经承租人或者出租人催告，在催告期满后仍未交付的。
     (三)承租人的义务
         承租人应当履行占有租赁物期间的维修义务。承租人占有租赁物期间，因租赁物造成第三人的人身伤害或者财产损害的，出租人不承担责任。
         出租人享有租赁物的所有权。
         出租人和承租人可以约定租赁期间届满租赁物的归属。没有约定→协议补充→交易习惯→出租人。
         承租人破产的，租赁物不属于破产财产。
         《解释》规定，承租人占有租赁物期间，租赁物毁损、灭失的风险由承租人承担，出租人要求承租人继续支付租金的，人民法院应予支持。但当事人另有约定或者法律另有规定的除外。
         出租人转让其在融资租赁合同项下的部分或者全部权利，受让方以此为由请求解除或者变更融资租赁合同的，人民法院不予支持。
 */

