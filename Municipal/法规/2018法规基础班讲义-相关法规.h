#pragma mark 抵押
/*
 
 (不转移对抵押物的占有；债务人或第三人都可以抵押；动产和不动产都可以抵押；抵押物不能直接归债权人所有，优先受偿)
 
 抵押(不转移对抵押物的占有)
 
 一、概念
        债务人或者第三人不转移对财产的占有，将该财产作为债权的担保。
        债务人不履行债务时，债权人有权以该财产折价或以拍卖、变卖该财产的价款优先受偿的担保方式。
        债务人或者第三人称为抵押人，债权人称为抵押权人。
 二、抵押物
     (一)可以抵押的财产
        1、建筑物和其他土地附着物;
        2、建设用地使用权;
        3、以招标、拍卖、公开协商等方式取得的荒地等土地承包经营权;
        4、生产设备、原材料、半成品、产品 ;
        5、正在建造的建筑物、船舶、航空器;
        6、交通运输工具;
        7、法律、行政法规 未禁止抵押的其他财产
     (二)不得抵押的财产 （公有的和有问题的不能抵押）
         1、土地所有权;
         2、耕地、宅基地、自留地、自留山等集体所有的土地使用权 ;
         3、学校、幼儿园、医院等以公益为目的的事业单位、社会团体的教育设施、医疗卫生设施和其他社会公益设施;
         4、所有权、使用权不明或有争议的财产;
         5、依法被查封、扣押、监管的财产。
     (三)抵押权的产生
         (1)不动产登记生效:建筑物;林木;建设用地使用权;荒地承包经营权;城市房地产;正在建造的建筑物。
         (2)动产合同生效时设立(未经登记不能对抗善意第三人):生产设备、原材料、半成品、产品;交通运输工具;正在建造的船舶、航空器。
 三、抵押权的效力
     (1)抵押担保的范围
        包括主债权及利息、违约金损害赔偿金和实现抵押权的费用。当事人也可以在抵押合同中约定抵押担保的范围。
     (2)抵押人有义务妥善保管抵押物并保证其价值。
         抵押人转让抵押物的价款，超过债权的部分归抵押人所有，不足部分由债务人清偿。
         转让抵押物的价款不得明显低于其价值。
         抵押人转让已登记抵押财产的，必须通知抵押权人并告知受让人，否则转让行为无效
     (3)抵押权与其担保的债权同时存在。
         抵押权不得与债权分离而单独转让或者作为其他债权的担保。
 
  四、抵押权的实现
 
        抵押权人折价或拍卖、变卖所得优先受偿
 
        实现顺序：（登记>顺序>比例）
             已登记的，按登记先后顺序;顺序相同，按比例清偿;
             未登记的，按合同生效先后顺序;顺序相同，按比例清偿;
             已登记的先于未登记清偿
 */

#pragma mark 质押
/*
 【抵押和质押都是优先受偿权 不能直接去的所有权】
 
 质押(必须转移对质押物的占有，这是与抵押最主要的区别)
 
 概念
    是指债务人或第三人将其动产或权利移交债权人占有，用以担保债权的实现， 当债务人不能履行债务时，债权人依法有权就该动产或权利优先得到清偿的担保法律行为。
 
 质权是一种约定的担保物权，以转移占有为特征。
 债务人或第三人为出质人，债权人为质权人，移交的动产或权利为质物。
 
 质押的分类:
    动产质押(能够用作质押的动产没有限制)
    权利质押:
         (1)汇票、 支票、本票、债券、存款单、仓单、提单;
         (2)依法可以转让的股份、股票;
         (3)依法可以转让的商标专用权、专利权、著作权中的财产权;
         (4)依法可以质押的 其他权利。
         注意:无房产证
 */

#pragma mark 留置权
