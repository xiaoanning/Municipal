#pragma mark 对施工质量负责和总分包单位的质量责任
/*
    一、施工单位对施工质量负责
         施工单位应当建立质量责任制，确定工程项目的项目经理、技术负责人和施工管理负责人
         建设工程质量责任与施工质量责任的主体不同
         施工单位是建设工程质量的重要责任主体，但不是唯一的责任主体
 二、总分包单位的质量责任
         建筑工程实行总承包的，工程质量由工程总承包单位负责。
         《建设工程质量管理条例》规定，总承包单位依法将建设工程分包给其他单位的，分包单位应当按照分包合同的约定对其分包工程的质量向总承包单位负责，总承包单位与分包单位对分包工程的质量承担连带责任。
         分包单位应当接受总承包单位的质量管理，就其分包工程，按分包合同的约定，向总承包单位负责。
         对外连带，对内追偿。
 
 */
#pragma mark 按照工程设计图纸和施工技术标准施工的规定
/*
 
     施工单位必须按照工程设计图纸和施工技术标准施工 ，发现有差错，及时提出意见;
     工程设计修改由原设计单位负责，施工单位不得擅自修改。
     施工单位必须按照工程设计要求、施工技术标准和合同约定，对建筑材料、建筑构配件、设备和商品混凝土进行检验，检验应当有书面记录和专人签字;未经检验或检验不合格的，不得使用。
     如果施工单位没有按照工程设计图纸和施工技术标准施工，则属于违约行为，应该对建设单位承担违约责任。
 
 */
#pragma mark 对建筑材料、设备等进行检验检测的规定
/*
 
     建筑施工企业必须按照【工程设计要求、施工技术标准和合同的约定】， 对建筑材料、建筑构配件、设备和商品混凝土进行检验，不合格的不得使用。
     涉及结构安全的试块、试件及有关材料，应在建设单位或监理单位监督下取样，并送相应检测机构检验
 
     见证取样和送检，是指在建设单位或工程监理单位人员的见证下， 由施工单位的现场试验人员对工程中【涉及结构安全】的试块、试件和材料在现场取样，并送至具有法定资格的质量检测单位进行检测的活动。
     见证取样和送检的比例不得低于应取样数量的【30%】

     必须【实施见证取样和送检】:
         用于承重结构的混凝土试块;
         用于承重墙体的砌筑砂浆试块;
         用于承重结构的钢筋及连接接头试件;
         用于承重墙的砖和混凝土小型砌块;
         用于拌制混凝土和砌筑砂浆的水泥;
         用于承重结构的混凝土中使用的掺加剂;
         地下、屋面、厕浴间使用的防水材料;
         国家规定其它试块、试件和材料。

     见证人员应由建设单位或监理单位中具备施工试验知识的专业技术人员担任，并由建设单位或监理单位书面通知施工单位、 检测单位和负责该工程的质量监督机构。
 
     (5)【取样人员】应在试样或其包装上作出标识、封志。标识和 封志应标明【工程名称、取样部位、取样日期、样品名称和样品数量】，并由【见证人员和取样人员】签字。
 
 (二)工程质量检测机构的资质和检测规定
     (1)工程质量检测机构是具有独立法人资格的中介机构。
     (2)检测机构资质分为专项检测机构资质和见证取样检测机构资质。
         检测机构未取得相应的资质证书，不得承担本办法规定的质量检测业务。
     (3)检测报告经检测人员签字、检测机构法定代表人或者其授权的签字人签署，并加盖检测机构公章或者检测专用章后方可生效。
         检测报告经建设单位或者工程监理单位确认后，由施工单位归档。
         质量检测业务由工程项目建设单位委托具有相应资质的检测机构进行检测。
 
         如果检测结果利害关系人对检测结果发生争议的，由双方共同认可的检测机构复检，复检结果由提出复检方报当地建设主管部门备案。

     (4)检测机构应当将检测过程中发现的建设单位、监理单位、施工单位违反有关法律、法规和工程建设强制性标准的情况，以及涉及结构安全检测结果的不合格情况，及时报告工程所在地建设主管部门。检测机构应当建立档案管理制度，并应当单独建立检测结果不合格项目台账。
     (5)检测人员不得同时受聘于两个或者两个以上的检测机构。检测机构和 检测人员不得推荐或者监制建筑材料、构配件和设备。检测机构不得与行政 机关，法律、法规授权的具有管理公共事务职能的组织以及所检测工程项目相关的设计单位、施工单位、监理单位有隶属关系或者其他利害关系。
     (6)检测机构不得转包检测业务。
 */
