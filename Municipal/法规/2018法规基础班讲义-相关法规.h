#pragma mark 工程建设标准的分类
/*
 工程建设标准的分类
 
     我国的标准分为国家标准、行业标准、 地方标准和企业标准。
 
     国家标准、行业标准分为强制性标准和推荐性标准。
         保障人体健康，人身、财产安全的标准和法律、行政法规规定强制执行的标准是强制性标准，其他的标准是推荐性标准。
 【强制性和推荐性的区分】
 
     工程建设国家标准的制订程序分为准备、征求意见、送审和报批四个阶段
 
 国家强制性标准:（通用的属于国家强制性标准）
     (1)工程建设勘察、规划、设计、施工(包括安装)及验收等通用的综合标准和重要的通用的质量标准
     (2)工程建设【通用】的有关安全、卫生和环境保护的标准
     (3)工程建设重要的通用的术语、符号、代号、计量与单位、建筑模数和制图方法标准
     (4)工程建设重要的通用的试验、检验和评定方法等标准
     (5)工程建设重要的通用的信息技术标准。
 一般国家标准【实施】后5年复审1次  （不是颁布后）
 
 行业标准强制性标准（行业的属于行业标准）
     (1)工程建设勘察、规划、设计、施工(包括安装)及验收等行业专用的综合性标准和重要的行业专用的质量标准
     (2)工程建设【行业】专用的有关安全、卫生和环境保护的标准
     (3)工程建设重要的行业专用的术语、符号、代号、计量与单位和制图方法标准
     (4)工程建设重要的行业专用的试验、检验和评定方法等标准
     (5)工程建设重要的行业专用的信息技术标准
 行业标准不得与国家标准相抵触。
 在公布国家标准之后，该项行业标准即行废止。
 一般实施后5年复审1次
 
 工程建设地方标准
     没有国家标准、行业标准或不具体，可制定地方标准
     对直接涉及人民财产安全、人体健康、环境保护和公共利益的条文，经国务院建设行政主管部门确定后，可作为强制性条文
     地方标准不得与国家标准和行业标准相抵触。相抵触的工程建设地方标准的规定，应当自行废止
 
 
 工程建设企业标准
     企业生产的产品没有国家标准和行业标准的，应当制定企业标准，作为组织生产的依据
     已有国家标准或者行业标准的，国家鼓励企业制定严于国家标准或者行业标准的企业标准，在企业内部适用
 */

