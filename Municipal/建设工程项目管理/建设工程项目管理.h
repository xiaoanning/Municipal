#pragma mark  1Z206031 单价合同的运用

/*

 当合同总价与各项单价乘以实际完成的工程量之和发生矛盾时，则肯定以后者为准，即单价优先。实际工程款的支付也将实际完成工程量乘以合同单价进行计算。
 
 
 单价合同适合工程量一时不能明确的工程。
 
 实际支付的工程款=实际完成的工程量*合同单价
 
 单价优先。有明显错误，业主【有权先修改再评标】。总价单价不一致，以单价为准调整总价。
 
 单价合同 业主和承包商都不存在工程量方面的风险。
 
 类型
    固定单价合同【不管什么情况都不变单价】
         适用于工期较短、工程量变化幅度不会太大的项目。【变化幅度 即 百分比】
    变动单价合同
         适用的三种情况:【实际工程量发生较大变化】、通货膨胀达到一定水平、国家政策发生变化。
 
 当采用变动单价合同时，合同双方可以约定一个估计的工程量，当实际工程量发生较 大变化时可以对单价进行调整，同时还应该约定如何对单价进行调整;当然也可以约定，当通货膨胀达到一定或国家政策发 生变化时，可以对哪些工程的内容的单价进行调整以及如何进行调整。
 
*/
#pragma mark 1Z206032 总价合同的运用

/*
 
 一、总价合同的含义
 
     也称总价包干合同，当施工内容和有关条件不变时，价款总额不变。
 
 二、固定总价合同
 
     承包商承担了全部的工程量和价格的风险。
 
     国际上，被广泛接受和采用
 
     价格风险:
         价算错，漏报，物价、人工费上涨;
     工作量风险:
         量算错，工程范围不确定，工程变更，设计深度不够。
 
     固定总价合同价格调整的情况:重大工程变更、累计工程变更超过一定幅度、其他特殊条件。【没有通货膨胀】
 
     采用固定总价合同，双方结算比较简单，但是由于承包商承担了较大的风险，因此报价中不可避免地要增加一笔较高的不可预见风险费。
 
     适用范围:
     (1)量小、期短;
     (2)设计详细，图纸完整;
     (3)工程结构和技术简单，风险小;
     (4)投标期相对宽裕。
     (5)施工期限一年左右的项目。
 
 三、变动总价合同——可调总价合同
 
     适用的四种情况:通货膨胀、设计变更、工程量变化、工程条件变化。
 
     建设周期一年半以上的项目，应考虑下列因素引起的价格变化:
     (1)劳务工资、材料费上涨;
     (2)运输费、燃料费、电力等价格变化;
     (3)外汇汇率不稳定;【外汇 不是银行汇率】
     (4)国家立法改变引起工程费用上涨。
 
 四、总价合同特点
 
     (1) 可以较早确定或者预测工程成本;
     (2) 【业主风险小，承包人风险大】;
     (3) 易于迅速确定最低报价的投标人;
     (4) 【进度】上能极大地调动承包人的积极性;
     (5) 发包单位能更容易、更有把握对项目进行控制;
     (6) 必须完整明确规定承包人的工作;
     (7) 必须将设计和施工的变化控制在最小限度内。
 
 */
#pragma mark 1Z206033 成本加酬金合同的运用

/*
 一、含义——成本补偿合同
 
     承包商不承担任何价格或工程量变化的风险。
 
 二、特点和适用条件
 
     1.适用条件(应用)
         (1)工程特别复杂，工程技术、结构方案不能预先确定，或尽管可以确定但不能竞争性招标，如研究开发项目;
         (2)时间特别紧迫，如抢险救灾工程;
         (3)施工总管、CM、项目管理合同、咨询服务合同。
 
     2.对业主的优点
 
         (1)分段施工缩短工期;
         (2)减少承包商对立情绪;
         (3)利用承包商的技术专家，弥补设计不足;
         (4)根据自身力量和需要，较深入地介入和控制施工和管理;
         (5)通过确定最大保证价格约束成本不超过限值，转移一部分风险。
 
     3.对承包商的特点
 
         优点:风险低，利润比较有保证
         缺点:合同不确定性大
 
 三、形式
 
 【考概念  考适用情况】
 
     (1)成本加固定费用合同——成本估计不准，可能变化不大。
 
         确定一笔固定数目的报酬金额作为管理费及利润，对人工、材料、机械台班等直接成本则实报实销。
 
         如果【设计变更或增加新项目】，固定报酬也要增加。
 
         在工程总成本一开始估计不准，可能变化不大的情况，可采用此合同形式。
 
     (2)成本加固定比例费用合同——很难描述工程范围，或工期紧迫，无法按常规编制招标文件招标。
 
         工程成本中直接费加一定比例的报酬费，报酬部分的比例在签订合同时由双方确定。
 
         这种方式的报酬费用总额随成本加大而增加，【不利于缩短工期和降低成本】
 
         一般在工程初期很难描述工作范围和性质，或工期紧迫，无法按常规编制招标文件招标时采用
 
 
     (3)成本加奖金合同——准备不充分，仅能制定一个估算指标。规定底点和顶点，最大罚款不超过最高酬金。
 
         奖金根据成本估算指标制定，在合同中规定一个底点和一个顶点，分别为工程成本估算的60%~75%和 110%~135%。
         当实际成本超过顶点对承包商罚款时，最大罚款限额不超过原先商定的最高酬金值。
         在招标时，当图纸、规范等准备不充分，不能据以确定合同价格，而仅能制定一个估算指标时采用。
 
     (4)最大成本加费用合同——设计深度达到可以报总价，包括管理费、风险费、利润。
         【在非代理型(风险型)CM 模式中应用】。
 
         在总价合同基础上加固定酬金的方式，即当设计深度达到可以报总价的深度，投标人报一个工程成本总价和一个固定的酬金(包括各项管理费、风险费和利润)。
         在非代理型(风险型)CM模式中应用
 
  (1) 成本加固定费用合同
 
         【根据这种合同，招标单位对投标人支付的人工、材料、设备台班费等直接成本全部予以补偿，同时还增加一笔管理费。
         所谓固定费用，是指杂项费用与利润相加的和，这笔费用总额是固定的， 只有当工程范围发生变更而超出招标文件的规定时才允许变动。】
 
         这种超出规定的范围是指在成本、 工时、 工期或其他可测项目方面的变更招标文件规定数量的上下 10%.
 
 (2) 成本加固定比例费用合同
 
         成本加固定比例费用合同与成本补偿合同相似， 不同的只不过是所增加的费用不是一笔固定金额，而是按照成本的一定比率计算的一个百分比份额。
 
 (3) 成本加奖金合同
 
         奖金是根据报价书的成本概算指标制定的，概算指标可以是总工程量的工时数的形式， 也可以是人工和材料成本的货币形式，
         在合同中，概算指标被规定了一个底点和一个顶点， 投标人在概算指标的顶点下完成工程时就可以得到奖金， 奖金的数额按照低于指标顶点的情 况而定;
         而如果投标人在工时或工料成本上超过指标顶点时， 他就应该对超出部分支付罚款， 直到总费用降低到概算指标的顶点为止。
 
 */
