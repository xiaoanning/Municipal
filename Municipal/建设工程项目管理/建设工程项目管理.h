/*
 
 一、合同订立的程序
 
     要约邀请——发布招标公告
     要约——提交投标文件
     承诺——发出中标通知书
 
 二、合同谈判的主要内容
 
     1. 谈判双方达成一致的内容，以“合同补遗”、“会议纪要”的方式，构成合同的一部分。
 
     2. 工期较长，容易货币贬值、通货膨胀，价格调整条款可以比较公正的解决承包人的风险损失。
 
         【合同计价方式以及价格调整方式】共同确定了合同的实际价格。
 
     3. 合同付款分四个阶段:预付款、进度款、最终付款、 退还保留金。
 
     4. 承包人应力争以维修保函代替保留金。
 
         维修保函对承包人有利，可提前取回现金，且保函有时效，期满自动作废。
         对业主无风险，真正发生维修费用，可凭保函向银行索回款项。
 
         对于具有较多单项工程的建设工程项目，可在合同中明确允许分部位或分批提交业主验收，并从该批验收时起开始计算该部分的维修期
         由于工程变更原因对工期产生不利影响时，应该给予承包人要求合理延长工期的权利
         承包人应该只承担由于材料和施工方法及操作工艺等不符合合同规定而产生的缺陷的维修责任
         业主和承包人可以【不是应当】根据项目情况、施工环境因素等商定适当的开工时间
 
 中标人与招标人可根据招标文件中要求的工期，或者根据投标人在投标文件中承诺的工期，并考虑工程范围和工程量的变动而产生的影响来商定一个确定的工期。
 同时，还要明确开工日期、竣工日期等。
 
 
 
 双方【可】根据【各自的项目准备情况、季节和施工环境因素】等条件洽商适当的开工时间。 【是可以洽商  不是应当洽商】
 
 对于具有较多的单项工程的建设工程项目，可在合同中明确允许【分部位或分批】提交业主验收（例如成批的房屋建筑工程应允许分栋验收；分多段的公路维修工程应允许分段验收；分多片的大型灌溉工程应允许分片验收等），并从该批验收时起开始计算该部分的维修期，以缩短承包人的责任期限，最大限度保障自己的利益。
 
 双方应通过谈判明确，由于工程变更（业主在工程实施中增减工程或改变设计等）、恶劣的气候影响，以及种种“作为一个有经验的承包人无法预料的工程施工条件的变化”等原因对工期产生不利影响时的解决办法，通常在上述情况下应该给予承包人要求【合理延长工期】的权利。
 
 合同文本中应当对维修工程的范围、维修责任及维修期的开始和结束时间有明确的规定，承包人应该【只承担由于材料和施工方法及操作工艺】等不符合合同规定而产生的缺陷。
 
 承包人应力争以【维修保函】来代替业主扣留的保留金。
 
 与保留金相比，维修保函对承包人有利，主要是因为可提前取回被扣留的现金，而且保函是有时效的，期满将自动作废。同时，它对业主并无风险，真正发生维修费用，业主可凭保函向银行索回款项。因此，这一做法是比较公平的。
 维修期满后，承包人应及时从业主处撤回保函。
 
 三、合同补遗与签订
 
     谈判结果一般以《合同补遗》，或《合同谈判纪要》的形式，形成书面文件。
 
     合同必须遵守法律，对于违反法律的条款，即使双方达成协议并签了字，也不受法律保障。
 
     谈判结束后，形成完整的合同文本草案，经双方代表认可后形成正式文件。【不是谈判结束 即形成正式文件】
 
 */
