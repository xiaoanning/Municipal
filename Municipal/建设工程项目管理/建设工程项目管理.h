/*
 
 二、施工成本分析的方法  【4个】
 
     (一)成本分析的【基本方法】【4个方法】  比较、因素分析、差额计算、比率（相关、构成、动态）
 
         1.比较法
 
             又称“指标对比分析法”，对比技术经济指标， 检查目标完成情况，分析产生差异的原因。
             (1)实际与目标比:检查完成情况，分析积极因素和消极因素
             (2)本期与上期比:反映提高降低程度
             (3)与平均、先进比:反映差距
 
         2.因素分析法
 
             又称连环置换法，分析各种因素（产量、单价、损耗率）对成本的影响程度。
             计算步骤:
             ● 确定分析对象
             ● 确定影响因素，并排序(排序规则:先实物量，后价值量;先绝对值，后相对值)
             ● 目标数相乘
             ● 替换计算
             ● 结果比较，确定影响程度
 
             计算思路:【注意减数和被减数  注意损耗率是百分数】
             已知:目标数A、B、C，实际数A1、B1、C1 则有:
             ● 目标:A*B*C ——————1
             ● 量变:A1*B*C——————2
             ● 价变:A1*B1*C —————3
             ● 率变:A1*B1*C1—————4
             ● 2-1:量变引起费变
             ● 3-2:价变引起费变
             ● 4-3:率变引起费变
             ● 4-1:量价率均变引起费变
             (即实际与目标差额)
 
 
         3.差额计算法——因素分析法的简化形式【最常考】
 
         4.比率法
 
             【用两个以上指标的【比例】进行分析】。
 
             (1)相关比率法:【性质不同且相关，如产值与工资】。
             (2)构成比率法:比重分析法或结构对比分析法，
                 考察【量、本、利，即预算、实际和降低成本比例关系】。
             (3)动态比率法:同类指标不同时期，基期指数、环比指数。
 
 
     (二)综合成本的分析方法【4个分析方法】
 
         1.分部分项工程成本分析
 
             (1)分部分项工程成本分析是项目成本分析的基础;
             (2)分析对象:已完工分部分项工程;
             (3)分析方法:预算、目标和实际成本“三算”;【是三个  不是两个 少个就不对】
             (4)资料来源:预算成本来自投标报价，目标成本来自施工预算，实际成本来自施工任务单。
             (5)无法也没必要对每个分部分项工程进行成本分析。但主要分部分项工程必须进行成本分析，而且从开工到竣工进行系统的成本分析。
 
         2.月(季)度成本分析
 
             (1)依据:当月(季)成本报表;
             (2)实际与预算比，分析降低水平;
             (3)实际与目标比，分析落实情况和管理中的问题和不足。
 
            1000万中标 目标责任书800万 实际900万
 
             在进行月季度成本分析时发现出现了属于预算定额规定的政策性亏损则应该 从【控制支出】 着手把 【超支额压缩】 到最低限度
 
         3.年度成本分析
 
             (1)一年结算一次，不得转入下一年度;
             (2)依据:年度成本报表;
             (3)重点:【除了月度分析内容以外】，年度成本分析的重点是针对下一年度施工进展制定可行的成本管理措施，保证成本目标实现。
 
         4.竣工成本的综合分析
 
             (1)以单位工程竣工成本分析资料为基础。
 
             (2)单位工程竣工成本分析的【内容】:
                 竣工成本分析;
                 主要资源节超对比分析;
                 主要技术节约措施及经济效果分析。【不是主要经济指标分析】
 
     (三)成本项目的分析方法 【4个】
 
         1.人工费
         2.材料费
 
             (1)主材结构件——受价格和消耗量影响
             (2)周转材——取决周转率和损耗率
             (3)采购保管费——分析保管费率的变化
             (4)材料储备资金——分析日平均用量、单价、储备天数对储备资金的影响程度。储备天数是关键因素。
 
         3.机械使用费
         4.管理费
 
     (四)专项成本分析方法 【3个方法】
 
         1.成本盈亏异常分析
             从 “三同步”入手。
             原因:经济核算基本规律是完成多少产值、消耗多少资源、发生多少成本之间有着同步关系。
             “三同步”适用于【盈亏异常、月度成本检查】。
             通过以下五方面进行分析:
             (1)产值与实际工程量和形象进度是否同步;
             (2)资源消耗与实耗人、材、机是否同步;
             (3)产值统计与实际支付是否同步;【产、支】
             (4)预算成本与产值统计是否同步;【预、产】
             (5)实际成本与资源消耗是否同步。【实、耗】
 
 
         2.工期成本分析
 
             计划工期成本与实际工期成本的比较分析。 先用比较法，再用“因素分析法”。
 
         3.资金成本分析
 
             应用“成本支出率”指标，即实际支出占实际收入的比例。
 

 
 */
