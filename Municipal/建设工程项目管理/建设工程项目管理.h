#pragma mark 1Z206064 工期索赔的计算【重要】

/*
 
 一、工期延误的分类
 
     1.按结果分——可索赔延误、不可索赔延误。
 
     2.按线路分——关键线路延误、非关键线路延误。
 
         关键线路，非承包商原因，可索赔。
 
         非关键线路，非承包商原因，取决于总时差大小和延误时间长短。
             如果延误时间少于总时差，业主一般不会给予工期顺延，但可能给予费用补偿;
             如果延误时间大于总时差，非关键线路工作转化为关键工作，成为可索赔延误。
 
     3.按关联性划分
 
            【每一种延误都是可能索赔、可能不索赔】
 
         (1)单一延误——某一延误事件从发生到终止，没有其他延误事件发生。——可不可赔，看谁责任。
         (2)共同延误——两个或两个以上延误事件从发生到终止的时间完全相同。
             ——延误事件全是业主的，可赔;
             ——延误事件全是承包商的，不可赔;
             ——延误事件业主和承包商都有，可赔变成不可赔。
         (3)交叉延误——当两个或两个以上延误事件从发 生到终止只有部分时间重合。——不重合部分，参照单一延误;重合部分，参照共同延误。
 
 三、工期索赔的计算方法
 
     1. 直接法
 
         干扰事件直接发生在关键线路上。
 
     2. 比例分析法【重点】
 
         (1)按工程量比例进行分析
 
                 某工程的工程量由2800m3增加到3500m3，原定工期 是40天。
                 工期索赔值=原工期×新增工程量/原工程量=40× (3500-2800)/2800=10天
 
                 如果工程量增减10%为承包商应承担的风险，则: 工期索赔值=40×(3500-2800×110%)/2800=6天
 
         (2)按造价比例进行分析
 
                 某工程合同价1200万元，总工期24个月，施工过程中 业主增加额外工程200万元。
                 工期索赔值=原合同工期×附加或新增工程造价/原合 同总价=24×200/1200=4个月。
 
     3. 网络分析法——标号法
 
 */
